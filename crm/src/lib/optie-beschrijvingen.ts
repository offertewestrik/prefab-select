/**
 * Verrijkt een regel-omschrijving met de bijbehorende beschrijving.
 * Zoekt de langst matchende optienaam in de tekst en plakt de beschrijving erachter.
 */
export function verrijkRegel(omschrijving: string): string {
  if (!omschrijving || omschrijving.includes("—")) return omschrijving;
  const norm = omschrijving.toLowerCase();
  let beste: string | null = null;
  for (const naam of Object.keys(OPTIE_BESCHRIJVINGEN)) {
    if (norm.includes(naam.toLowerCase()) && (!beste || naam.length > beste.length)) {
      beste = naam;
    }
  }
  return beste ? `${omschrijving} — ${OPTIE_BESCHRIJVINGEN[beste]}` : omschrijving;
}
